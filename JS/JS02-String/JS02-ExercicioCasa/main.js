let frase = "Estamos aprendendo JavaScript e seremos futuros Devs";

// Caracter na posição 19 (lembrando que começa do 0)
console.log("O caracter que está na posição 19 é: " + frase.charAt(19));

// Tamanho da string
console.log("O tamanho de toda a String é: " + frase.length);

// String toda em maiúsculo
console.log("Toda a String em maiúsculo: " + frase.toUpperCase());